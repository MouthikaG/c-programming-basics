#include<stdio.h>
int main()
{
    char p='a';
    char q='B';
    char r='c';
    char digit='2';
    char special_char='@';
    
    printf("p=%c\n",p);
    printf("q=%c\n",q);
    printf("r=%c\n",r);
    printf("digit=%c\n",digit);
    printf("special_char=%c\n",special_char);
    
    printf("ASCII value for p=%d\n",p);
    printf("ASCII value for q=%d\n",q);
    printf("ASCII value for r=%d\n",r);
    printf("ASCII value for digit=%d\n",digit);
    printf("ASCII value for special_char=%d\n",special_char);
    return 0;
}
